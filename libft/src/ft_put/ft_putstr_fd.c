#include "libft.h"

/*
s: The string to output.
fd: The file descriptor on which to write.
return: none
Outputs the string â€™sâ€™ to the given file
descriptor.
*/
void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	if (!s)
		return ;
	i = ft_strlen(s);
	write(fd, s, i);
}
